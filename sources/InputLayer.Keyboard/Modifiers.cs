namespace InputLayer.Keyboard
{
    public enum Modifiers : ushort
    {
        None = 0x00,
        LeftShift = 0xA0,
        RightShift = 0xA1,
        LeftControl = 0xA2,
        RightControl = 0xA3,
        LeftAlt = 0xA4,
        RightAlt = 0xA5,
        LeftWin = 0x5B,
        RightWin = 0x5C
    }
}